// 메모리 이론

// 자바스크립트는 메모리를 자동으로 관리합니다.
// 개발자는 메모리 할당과 해제를 직접 신경쓰지 않아도 됩니다.

// 하지만, 메모리 사용량을 최소화하고 성능을 최적화하기 위해서는
// 메모리 관리에 대한 기본적인 이해가 필요합니다.

// 1. 메모리 할당
// 변수, 객체, 배열 등을 생성할 때 자바스크립트 엔진은 필요한 메모리를 자동으로 할당합니다.
let num = 42; // 숫자형 변수에 메모리 할당
let str = "Hello, World!"; // 문자열 변수에 메모리 할당
let obj = { name: "Alice", age: 30 }; // 객체에 메모리 할당

// 2. 가비지 컬렉션 (Garbage Collection)
// 자바스크립트는 가비지 컬렉터라는 메커니즘을 통해 더 이상 참조되지 않는 메모리를 자동으로 해제
// 합니다. 이를 통해 메모리 누수를 방지하고 효율적인 메모리 사용을 유지합니다.

// 예를 들어, 아래와 같이 변수를 null로 설정하면
// 해당 메모리는 더 이상 참조되지 않으므로 가비지 컬렉터가 이를 해제할 수 있습니다.
obj = null; // 객체에 대한 참조 제거

// 3. 메모리 최적화 팁
// - 불필요한 변수나 객체는 null로 설정하여 가비지 컬렉션이 가능하도록 합니다.
// - 큰 데이터 구조를 다룰 때는 필요할 때만 생성하고, 사용 후에는 참조를 제거합니다.
// - 클로저(Closure)를 사용할 때는 외부 함수의 변수를 과도하게 참조하지 않도록 주의합니다.

// 요약
// 자바스크립트는 메모리를 자동으로 관리하지만, 개발자는 메모리 사용에 대한 기본적인 이해를 통해
// 효율적인 코드를 작성할 수 있습니다. 가비지 컬렉션을 활용하고, 불필요한 참조를 제거하여
// 메모리 누수를 방지하는 것이 중요합니다.

